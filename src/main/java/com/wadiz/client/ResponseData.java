package com.wadiz.client;

/**
 * 응답데이터 클래스
 * @author hyunlae
 *
 */
public interface ResponseData {

	/**
	 * 응답전문데이터를 Json 문자열로 변환한다.
	 * @return
	 */
	public String toJson();
	
	/**
	 * 응답전문데이터를 Object로 가져온다.
	 * @return
	 */
	public Object getData();
	
	/**
	 * 응답된 데이터를 세팅한다.
	 * @param data
	 */
	public void setData(Object data);
	
}
